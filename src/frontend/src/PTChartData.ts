/**
 * Returns saturation temperature in °F for the given refrigerant at the given PSI.
 * Uses linear interpolation between table points.
 * Returns null if PSI is out of range for the selected refrigerant.
 */
export function ptLookup(refrigerantId: string, psi: number): number | null {
  const table = PT_TABLES[refrigerantId];
  if (!table || table.length === 0) return null;

  const minPsi = table[0][0];
  const maxPsi = table[table.length - 1][0];

  if (psi < minPsi || psi > maxPsi) return null;

  // Find surrounding points
  for (let i = 0; i < table.length - 1; i++) {
    const [p0, t0] = table[i];
    const [p1, t1] = table[i + 1];
    if (psi >= p0 && psi <= p1) {
      if (p0 === p1) return t0;
      // Linear interpolation
      const ratio = (psi - p0) / (p1 - p0);
      return Math.round((t0 + ratio * (t1 - t0)) * 10) / 10;
    }
  }

  return null;
}

export function getPsiRange(
  refrigerantId: string,
): { min: number; max: number } | null {
  const table = PT_TABLES[refrigerantId];
  if (!table || table.length === 0) return null;
  return { min: table[0][0], max: table[table.length - 1][0] };
}
